    private void txtSearchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSearchKeyReleased
        // TODO add your handling code here:
        ArrayList<newProductCode> list = new ArrayList<newProductCode>();
        String search = txtSearch.getText().trim();
        try {
            Connection con;
            Statement st;
            con = db.getConnection();
            st = con.createStatement();
            String query = "Select * from product where p_name like '%" + search + "%'";
            ResultSet rs = st.executeQuery(query);
            newProductCode cus;
            while (rs.next()) {
                cus = new newProductCode(rs.getInt("tra_id"), rs.getInt("c_id"), rs.getInt("p_id"), rs.getString("p_name"), rs.getInt("quantity"), rs.getDouble("p_unit"), rs.getString("date"), rs.getDouble("price"), rs.getString("region"));
                list.add(cus);
            }

            DefaultTableModel model = (DefaultTableModel) tblProduct.getModel();
            model.setRowCount(0);

            Object[] row = new Object[9];

            for (int i = 0; i < list.size(); i++) {
                row[0] = list.get(i).getTra_id();
                row[1] = list.get(i).getC_id();
                row[2] = list.get(i).getP_id();
                row[3] = list.get(i).getP_name();
                row[4] = list.get(i).getQuantity();
                row[5] = list.get(i).getP_unit();
                row[6] = list.get(i).getDate();
                row[7] = list.get(i).getPrice();
                row[8] = list.get(i).getRegion();

                model.addRow(row);
            }

            sum();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_txtSearchKeyReleased

