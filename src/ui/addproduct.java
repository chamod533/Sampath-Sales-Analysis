    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) tblProduct.getModel();

        if (model.getRowCount() == 0) {
            JOptionPane.showMessageDialog(this, "No data to display in chart!");
            return;
        }

        // ✅ Collect sales by region
        Map<String, Double> regionSalesMap = new HashMap<>();

        for (int i = 0; i < model.getRowCount(); i++) {
            String region = model.getValueAt(i, 8).toString();
            double totalPrice = Double.parseDouble(model.getValueAt(i, 7).toString());
            regionSalesMap.put(region, regionSalesMap.getOrDefault(region, 0.0) + totalPrice);
        }

        // ✅ Build the dataset
        DefaultPieDataset pie = new DefaultPieDataset();
        for (Map.Entry<String, Double> entry : regionSalesMap.entrySet()) {
            pie.setValue(entry.getKey(), entry.getValue());
        }

        // ✅ Create the pie chart
        JFreeChart chart = ChartFactory.createPieChart(
                "Sales by Region",
                pie,
                true,
                true,
                false
        );

        // ✅ Style the chart
        PiePlot plot = (PiePlot) chart.getPlot();
        plot.setBackgroundPaint(Color.WHITE);
        plot.setOutlineVisible(false);
        plot.setLabelFont(new Font("Segoe UI", Font.PLAIN, 14));
        plot.setLabelPaint(Color.DARK_GRAY);

        // ✅ Display chart
        ChartFrame chartFrame = new ChartFrame("Sales Pie Chart", chart);
        chartFrame.setSize(500, 450);
        chartFrame.setLocationRelativeTo(null);
        chartFrame.setVisible(true);
    }//GEN-LAST:event_jButton8ActionPerformed

