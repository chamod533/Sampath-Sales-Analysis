    public void saveData() {
        try {
            Connection con = db.getConnection();
            String query = "INSERT INTO product (tra_id, c_id, p_id, p_name, quantity, p_unit, date, price, region) VALUES(?,?,?,?,?,?,?,?,?)";
            PreparedStatement pst = con.prepareStatement(query);

            DefaultTableModel model = (DefaultTableModel) tblProduct.getModel();
            for (int i = 0; i < model.getRowCount(); i++) {
                pst.setInt(1, Integer.parseInt(model.getValueAt(i, 0).toString()));
                pst.setInt(2, Integer.parseInt(model.getValueAt(i, 1).toString()));
                pst.setInt(3, Integer.parseInt(model.getValueAt(i, 2).toString()));
                pst.setString(4, model.getValueAt(i, 3).toString());
                pst.setInt(5, Integer.parseInt(model.getValueAt(i, 4).toString()));
                pst.setDouble(6, Double.parseDouble(model.getValueAt(i, 5).toString()));
                pst.setString(7, model.getValueAt(i, 6).toString());
                pst.setDouble(8, Double.parseDouble(model.getValueAt(i, 7).toString()));
                pst.setString(9, model.getValueAt(i, 8).toString());
                pst.addBatch();
            }
            pst.executeBatch();
            JOptionPane.showMessageDialog(null, "Data saved successfully!");
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }


