    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        DefaultTableModel model = (DefaultTableModel) tblProduct.getModel();

        if (model.getRowCount() == 0) {
            JOptionPane.showMessageDialog(this, "No data to analyze!");
            return;
        }

        Map<String, Integer> quantityMap = new HashMap<>();
        Map<String, Double> revenueMap = new HashMap<>();
        Map<String, Double> regionSalesMap = new HashMap<>();
        double totalSales = 0;

        for (int i = 0; i < model.getRowCount(); i++) {
            String productName = model.getValueAt(i, 3).toString();
            int quantity = Integer.parseInt(model.getValueAt(i, 4).toString());
            double totalPrice = Double.parseDouble(model.getValueAt(i, 7).toString());
            String region = model.getValueAt(i, 8).toString();

            totalSales += totalPrice;

            quantityMap.put(productName, quantityMap.getOrDefault(productName, 0) + quantity);
            revenueMap.put(productName, revenueMap.getOrDefault(productName, 0.0) + totalPrice);
            regionSalesMap.put(region, regionSalesMap.getOrDefault(region, 0.0) + totalPrice);
        }

        String bestSellingProduct = Collections.max(quantityMap.entrySet(), Map.Entry.comparingByValue()).getKey();
        String topRevenueProduct = Collections.max(revenueMap.entrySet(), Map.Entry.comparingByValue()).getKey();

// Build HTML table for region sales
        StringBuilder regionTable = new StringBuilder();
        regionTable.append("<table border='1' cellpadding='8' cellspacing='1'>")
                .append("<tr><th>Region</th><th>Sales (Rs.)</th></tr>");
        for (Map.Entry<String, Double> entry : regionSalesMap.entrySet()) {
            regionTable.append("<tr>")
                    .append("<td>").append(entry.getKey()).append("</td>")
                    .append("<td>").append(String.format("%.2f", entry.getValue())).append("</td>")
                    .append("</tr>");
        }
        regionTable.append("</table>");

// Final HTML message
        String message = "<html>"
                + "<h2 style='text-align:center;'>Sales Analysis</h2>"
                + "<table cellpadding='5' cellspacing='0'>"
                + "<tr><td><b>Total Sales:</b></td><td>Rs. " + String.format("%.2f", totalSales) + "</td></tr>"
                + "<tr><td><b>Best-Selling Product (Quantity):</b></td><td>" + bestSellingProduct + "</td></tr>"
                + "<tr><td><b>Top Revenue Product:</b></td><td>" + topRevenueProduct + "</td></tr>"
                + "</table><br>"
                + "<b>Sales by Region:</b><br>"
                + regionTable.toString()
                + "</html>";

        JOptionPane.showMessageDialog(this, message, "Sales Analysis", JOptionPane.INFORMATION_MESSAGE);

    }//GEN-LAST:event_jButton4ActionPerformed

